    private void submitEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitEditActionPerformed
        
        String sname=editName.getText();
        String ssex=(String)editSex.getSelectedItem();
        Integer sage = (int)editAge.getValue();
        int sid = (int)sportsTable.getModel().getValueAt(sportsTable.getSelectedRow(), 0);
        try{
            if(sname.equals(""))
                throw new Exception("Please enter a name.");
            if(ssex.equalsIgnoreCase("Any"))
                ssex = null;
            if((sage==0))
                sage = null;
        PreparedStatement ps = cn.prepareStatement("SELECT * FROM edit_sports(?, ?, ?, ?)");
        ps.setObject(1, sid);
        ps.setObject(2, sname);
        ps.setObject(3, ssex);
        ps.setObject(4, sage);
        ps.executeQuery();
        
        insertFrame.setVisible(false);
        insertFrame.dispose();
        closePopup();
        refreshSports();
        } catch(Exception e){
            showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_submitEditActionPerformed

    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        try{Class.forName(SportsDB.driverClassName);
            
            PreparedStatement ps = cn.prepareStatement("SELECT * FROM select_sports(?, ?, ?, ?)");
            int sort;            
            String sex;
            Integer age;
            String name;
            DefaultTableModel model = (DefaultTableModel) sportsTable.getModel();
            if(nameRadioButton.isSelected())
                sort=2;
            else
                sort=1;
            
            if(nameTextfield.getText().equals(""))
                name=null;
            else
                name=nameTextfield.getText();
            
            if(sexCombobox.getSelectedIndex()==2)
                sex=null;
            else
                sex=(String) sexCombobox.getSelectedItem();
            
            if((int)ageSpinner.getValue()<=0)
                age= null;
            else
                age=(int)ageSpinner.getValue();
            ps.setObject(1, sort);
            ps.setObject(2, sex);
            ps.setObject(3, age);
            ps.setObject(4, name);
            
            ResultSet rs = ps.executeQuery();
            while(model.getRowCount()>0)
                model.removeRow(0);
            while(rs.next()){
                int tempid = rs.getInt("sportid");
                String tempname = rs.getString("sportname");
                String tempsex = rs.getString("sportsex");
                Integer tempage;
                if(rs.getInt("sportage")>0)
                    tempage=rs.getInt("sportage");
                else
                    tempage=null;
                model.addRow(new Object[]{tempid, tempname, tempsex, tempage});
            }
        }catch(Exception e){
        showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_searchButtonActionPerformed

