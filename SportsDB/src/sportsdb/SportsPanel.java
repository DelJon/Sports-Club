    private void refreshSports(){
        DefaultTableModel model = (DefaultTableModel) sportsTable.getModel();
        try{Class.forName(SportsDB.driverClassName);
            while(model.getRowCount()>0)
                model.removeRow(0);
            Statement statement = cn.createStatement();
            ResultSet rs = statement.executeQuery("SELECT * FROM select_sports(1, NULL, NULL,NULL)");
            while(rs.next()){
                int id = rs.getInt("sportid");
                String name = rs.getString("sportname");
                String sex = rs.getString("sportsex");
                Integer age;
                if(rs.getInt("sportage")>0)
                    age=rs.getInt("sportage");
                else
                    age=null;
                model.addRow(new Object[]{id, name, sex, age});
            }
            if(sportsTable.getRowCount()>0)
                sportsTable.setRowSelectionInterval(0, 0);
            else
                showMessageDialog(null, "No entries for table, please add some!");
            nameTextfield.setText("");
            sexCombobox.setSelectedIndex(2);
            ageSpinner.setValue(0);
            idRadioButton.setSelected(true);
        }catch(Exception e){
         showMessageDialog(null, e.getMessage());
        }
    }
    
