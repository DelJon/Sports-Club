    private void editSportButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editSportButtonActionPerformed
        try{
            if(sportsTable.getRowCount()<=0){
                throw new Exception("No entries to edit!");
            }
            int row = sportsTable.getSelectedRow();
            int selection = (int)sportsTable.getModel().getValueAt(row, 0);
            editFrame.pack();
            editFrame.setVisible(true);
            openPopup();
            editFrame.setTitle("Editing sport "+selection);
            
            editName.setText((String)(sportsTable.getModel().getValueAt(row, 1)));
            if (sportsTable.getModel().getValueAt(row, 2)==null)
                editSex.setSelectedIndex(2);
            else if(((String)(sportsTable.getModel().getValueAt(row, 2))).equalsIgnoreCase("Male"))
                editSex.setSelectedIndex(0);
            else if(((String)(sportsTable.getModel().getValueAt(row, 2))).equalsIgnoreCase("Female"))
                editSex.setSelectedIndex(1);
            else
                editSex.setSelectedIndex(2);
            
            if (sportsTable.getModel().getValueAt(row, 3)==null)
                editAge.setValue(0);
            else
                editAge.setValue((int)sportsTable.getModel().getValueAt(row, 3));
            
        } catch(Exception e){
            showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_editSportButtonActionPerformed

