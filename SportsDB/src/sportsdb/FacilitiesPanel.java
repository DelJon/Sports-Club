    private void refreshFacilities(){
        DefaultTableModel model = (DefaultTableModel) facilitiesTable.getModel();
        try{Class.forName(SportsDB.driverClassName);
            while(model.getRowCount()>0)
                model.removeRow(0);
            Statement statement = cn.createStatement();
            ResultSet rs = statement.executeQuery("SELECT * FROM select_facilities(1, NULL, NULL, TRUE)");
            while(rs.next()){
                int id = rs.getInt("faid");
                String size = rs.getInt("fasize")+" mÂ²";
                String name = rs.getString("faname");
                String location = rs.getString("falocation");
                String active;
                if(rs.getInt("faactive")==1)
                    active="YES";                    
                else
                    active="NO";
                model.addRow(new Object[]{id, size, name, location, active});
            }
            if(facilitiesTable.getRowCount()>0)
                facilitiesTable.setRowSelectionInterval(0, 0);
            else
                showMessageDialog(null, "No entries for table, please add some!");
            nameTextfield.setText("");
            locationCombobox.setSelectedIndex(2);
            activeCheckBox.setSelected(false);
            idRadioButton.setSelected(true);
        }catch(Exception e){
         showMessageDialog(null, e.getMessage());
        }
    }

