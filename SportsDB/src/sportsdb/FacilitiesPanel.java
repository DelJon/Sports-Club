    private void editFacilityButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editFacilityButtonActionPerformed
        try{
            editFrame.pack();
            if(facilitiesTable.getRowCount()<=0){
                throw new Exception("No entries to edit!");
            }
            int row = facilitiesTable.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) facilitiesTable.getModel();
            int selection = (int)model.getValueAt(row, 0);
            editFrame.setVisible(true);
            editFrame.setTitle("Editing facility "+selection);    
            if(((String)model.getValueAt(row, 4)).equals("YES")){
                activateButton.setText("Decomission");
                activeLabel.setText("YES");
            }   
            else{
                activateButton.setText("Restore");
                activeLabel.setText("NO");
            }
            editSize.setValue(Integer.valueOf(((String)(model.getValueAt(row, 1))).split("\\s")[0]));
            editName.setText((String)(model.getValueAt(row, 2)));
            if(((String)(model.getValueAt(row, 3))).equalsIgnoreCase("Outdoors"))
                editLocation.setSelectedIndex(1);
            else
                editLocation.setSelectedIndex(0);

            openPopup();
            editFrame.setVisible(true);
        } catch(Exception e){
            showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_editFacilityButtonActionPerformed

